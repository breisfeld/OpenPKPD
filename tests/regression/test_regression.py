"""
Regression tests for stable high-value estimation workflows.

Current regression matrix:
  - FOCE (golden reference + invariants)
  - Laplacian (golden reference + invariants)
  - SAEM (golden reference + invariants)
  - IMP (golden reference + invariants)
  - BAYES / Laplace fallback (golden reference + invariants)
  - Nonparametric estimation (golden reference + support-point invariants)

Reference philosophy:
  - Golden files are self-consistent openpkpd baselines stored under
    tests/regression/reference_runs/.
  - Stochastic methods use fixed seeds and tolerance-based assertions on stable
    summary metrics rather than exact trajectory equality.
  - Each golden-backed test also keeps at least one implementation-independent
    invariant (finite OFV, weights sum to one, PSD covariance, etc.).
"""

