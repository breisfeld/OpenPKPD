# ── 3d: Nonparametric convergence ─────────────────────────────────────────────


@pytest.mark.regression
@pytest.mark.slow
class TestNonparametricRegression:
    """Nonparametric estimation regression against self-consistent golden file."""

    @pytest.fixture(scope="class")
    def np_result(self):
        _, params, pop_model = _build_theophylline_model()
        method = NonparametricMethod(base_method="FOCE", max_iter=100)
        return method.estimate(pop_model, params)

    def test_weights_sum_to_one(self, np_result):
        total = float(np.sum(np_result.support_weights))
        assert abs(total - 1.0) < 1e-6, f"Support weights sum to {total}, expected 1.0"

    def test_ofv_matches_reference(self, np_result):
        ref = load_reference("theophylline_nonparametric")
        assert abs(np_result.ofv - ref["ofv"]) < 1.0, (
            f"Nonparametric OFV {np_result.ofv:.4f} differs from reference "
            f"{ref['ofv']:.4f} by {abs(np_result.ofv - ref['ofv']):.4f}"
        )

    def test_theta_matches_reference(self, np_result):
        ref = load_reference("theophylline_nonparametric")
        np.testing.assert_allclose(
            np_result.theta_final,
            ref["theta"],
            rtol=0.02,
            err_msg="Nonparametric THETA deviates >2% from reference",
        )

    def test_empirical_moments_match_reference(self, np_result):
        ref = load_reference("theophylline_nonparametric")
        np.testing.assert_allclose(
            np_result.empirical_mean(),
            ref["empirical_mean"],
            atol=1e-3,
            err_msg="Nonparametric empirical ETA mean drifted from reference",
        )
        np.testing.assert_allclose(
            np_result.empirical_variance(),
            ref["empirical_variance"],
            atol=1e-3,
            err_msg="Nonparametric empirical ETA variance drifted from reference",
        )

    def test_empirical_omega_psd(self, np_result):
        np_result.support_points.shape[1]
        emp_mean = np_result.empirical_mean()
        sp = np_result.support_points
        w = np_result.support_weights
        emp_cov = sum(w[k] * np.outer(sp[k] - emp_mean, sp[k] - emp_mean) for k in range(len(w)))
        eigvals = np.linalg.eigvalsh(emp_cov)
        assert np.all(eigvals >= -1e-8), f"Empirical OMEGA not PSD: {eigvals}"

    def test_ofv_finite(self, np_result):
        assert np.isfinite(np_result.ofv), f"Nonparametric OFV not finite: {np_result.ofv}"

    def test_n_support_points_equals_n_subjects(self, np_result):
        assert len(np_result.support_weights) == 12, (
            f"Expected 12 support points (one per subject), got {len(np_result.support_weights)}"
        )


