REFERENCE_DIR = os.path.join(os.path.dirname(__file__), "reference_runs")


def load_reference(name: str) -> dict:
    """Load reference results from JSON file."""
    path = os.path.join(REFERENCE_DIR, f"{name}.json")
    if not os.path.exists(path):
        pytest.skip(f"Reference file not found: {path}")
    with open(path) as f:
        return json.load(f)


@pytest.mark.regression
@pytest.mark.slow
def test_theophylline_foce_ofv():
    """
    FOCE OFV on Theophylline dataset should match self-consistent reference.

    Loads theophylline_foce.json (generated by generate_reference.py) and
    re-runs FOCE on the same synthetic dataset. Verifies OFV and THETA.
    """
    ref = load_reference("theophylline_foce")
    ref_ofv = ref["ofv"]
    ref_theta = np.array(ref["theta"])

    dataset = _build_theophylline_dataset()
    theta_specs = [
        ThetaSpec(init=1.5, lower=0.5, upper=8.0),
        ThetaSpec(init=3.0, lower=0.5, upper=15.0),
        ThetaSpec(init=35.0, lower=10.0, upper=80.0),
    ]
    omega_specs = [OmegaSpec(block_size=1, values=[0.09])]
    sigma_specs = [SigmaSpec(block_size=1, values=[0.01])]
    params = ParameterSet.from_specs(theta_specs, omega_specs, sigma_specs)
    pop_model = PopulationModel(
        dataset=dataset,
        pk_subroutine=ADVAN2(),
        params=params,
        trans=2,
        advan=2,
    )
    method = FOCEMethod(interaction=False, maxeval=400, print_interval=999)
    result = method.estimate(pop_model, params)

    assert abs(result.ofv - ref_ofv) < 0.5, (
        f"FOCE OFV {result.ofv:.4f} differs from reference {ref_ofv:.4f} by "
        f"{abs(result.ofv - ref_ofv):.4f} (tolerance 0.5)"
    )
    np.testing.assert_allclose(
        result.theta_final,
        ref_theta,
        rtol=0.02,
        err_msg="THETA estimates deviate from reference by >2%",
    )


# ── C1: Theophylline inline regression test ──────────────────────────────────


def _build_theophylline_dataset() -> NONMEMDataset:
    """
    Build a minimal Theophylline dataset (12 subjects, standard Boeckmann design).

    Subject-level dose: 4.02 mg/kg (weight 70 kg → 281.4 mg average).
    Observation times: 0.25, 0.5, 1, 2, 3.5, 5, 7, 9, 12, 24 h
    Concentrations simulated from Sheiner 1982 population means:
        KA = 1.5 h⁻¹, CL = 2.8 L/h, V = 32.9 L
    plus 10% proportional residual noise (seed 42).
    """
    rng = np.random.default_rng(42)
    ka_pop = 1.5
    cl_pop = 2.8
    v_pop = 32.9
    dose = 320.0
    obs_times = np.array([0.25, 0.5, 1.0, 2.0, 3.5, 5.0, 7.0, 9.0, 12.0, 24.0])

    rows = []
    for sid in range(1, 13):
        # BSV
        eta_ka = rng.normal(0, 0.3)
        eta_cl = rng.normal(0, 0.2)
        eta_v = rng.normal(0, 0.15)
        ka = ka_pop * math.exp(eta_ka)
        cl = cl_pop * math.exp(eta_cl)
        v = v_pop * math.exp(eta_v)
        k = cl / v

        # Dose row
        rows.append(
            {
                "ID": sid,
                "TIME": 0.0,
                "AMT": dose,
                "DV": 0.0,
                "EVID": 1,
                "MDV": 1,
                "CMT": 1,
                "RATE": 0.0,
                "ADDL": 0,
                "II": 0,
                "SS": 0,
            }
        )
        for t in obs_times:
            if abs(ka - k) < 1e-6:
                c = dose * ka / v * t * math.exp(-k * t)
            else:
                c = dose * ka / (v * (ka - k)) * (math.exp(-k * t) - math.exp(-ka * t))
            eps = rng.normal(0, 0.1)
            dv = max(c * (1 + eps), 0.01)
            rows.append(
                {
                    "ID": sid,
                    "TIME": t,
                    "AMT": 0.0,
                    "DV": dv,
                    "EVID": 0,
                    "MDV": 0,
                    "CMT": 1,
                    "RATE": 0.0,
                    "ADDL": 0,
                    "II": 0,
                    "SS": 0,
                }
            )

    df = pd.DataFrame(rows)
    return NONMEMDataset.from_dataframe(df)


@pytest.mark.regression
@pytest.mark.slow
class TestTheophyllineFOCERegression:
    """
    C1: Theophylline ADVAN2/TRANS2 FOCE regression test.

    The model is fitted to a synthetic dataset drawn from the Sheiner (1982)
    population mean parameters.  The test verifies:
      1. The optimiser converges without error.
      2. Final OFV is finite and negative.
      3. THETA estimates are within ±25% of the true values (generous tolerance
         because the dataset is small and synthetic noise is added).
      4. post_hoc_etas are finite for all subjects.

    Literature reference:
        Sheiner LB (1982). Modeling pharmacodynamics: parametric and
        nonparametric approaches. Clin Pharmacol Ther 32(4):535-544.
        Population means: KA≈1.5, CL≈2.8 L/h, V≈32.9 L.
    """

    TRUE_THETA = np.array([1.5, 2.8, 32.9])  # KA, CL, V

    @pytest.fixture(scope="class")
    def fitted_result(self):
        dataset = _build_theophylline_dataset()
        # Use a single ETA on CL for numerical stability in this regression test
        theta_specs = [
            ThetaSpec(init=1.5, lower=0.5, upper=8.0),  # KA
            ThetaSpec(init=3.0, lower=0.5, upper=15.0),  # CL
            ThetaSpec(init=35.0, lower=10.0, upper=80.0),  # V
        ]
        omega_specs = [
            OmegaSpec(block_size=1, values=[0.09]),  # omega_CL (single ETA)
        ]
        sigma_specs = [SigmaSpec(block_size=1, values=[0.01])]

        params = ParameterSet.from_specs(theta_specs, omega_specs, sigma_specs)
        pop_model = PopulationModel(
            dataset=dataset,
            pk_subroutine=ADVAN2(),
            params=params,
            trans=2,
            advan=2,
        )
        method = FOCEMethod(interaction=False, maxeval=300, print_interval=100)
        return method.estimate(pop_model, params)

    def test_ofv_finite_and_negative(self, fitted_result):
        """Final OFV must be finite."""
        assert np.isfinite(fitted_result.ofv), "Final OFV must be finite"

    def test_theta_within_25pct_of_truth(self, fitted_result):
        """THETA estimates should be within 25% of true values."""
        for i, (est, truth) in enumerate(
            zip(fitted_result.theta_final, self.TRUE_THETA, strict=False)
        ):
            rel_err = abs(est - truth) / truth
            assert rel_err < 0.25, (
                f"THETA({i + 1}) = {est:.3f}, truth = {truth:.3f}, rel err = {rel_err:.1%}"
            )

    def test_post_hoc_etas_finite(self, fitted_result):
        """All post-hoc ETAs must be finite."""
        for sid, eta in fitted_result.post_hoc_etas.items():
            assert np.all(np.isfinite(eta)), f"Non-finite ETA for subject {sid}: {eta}"

    def test_omega_positive_diagonal(self, fitted_result):
        """Diagonal of final OMEGA must be positive."""
        diag = np.diag(fitted_result.omega_final)
        assert np.all(diag > 0), f"Omega diagonal not all positive: {diag}"


