@pytest.mark.regression
@pytest.mark.slow
class TestLaplacianRegression:
    """Laplacian estimation regression against self-consistent golden file."""

    @pytest.fixture(scope="class")
    def foce_result(self):
        _, params, pop_model = _build_theophylline_model()
        method = FOCEMethod(interaction=False, maxeval=400, print_interval=999)
        return method.estimate(pop_model, params)

    @pytest.fixture(scope="class")
    def lap_result(self):
        _, params, pop_model = _build_theophylline_model()
        method = LaplacianMethod(interaction=True, maxeval=400, print_interval=999)
        return method.estimate(pop_model, params)

    def test_laplacian_ofv_matches_reference(self, lap_result):
        ref = load_reference("theophylline_laplacian")
        assert abs(lap_result.ofv - ref["ofv"]) < 1.0, (
            f"Laplacian OFV {lap_result.ofv:.4f} differs from reference "
            f"{ref['ofv']:.4f} by {abs(lap_result.ofv - ref['ofv']):.4f}"
        )

    def test_laplacian_theta_matches_reference(self, lap_result):
        ref = load_reference("theophylline_laplacian")
        np.testing.assert_allclose(
            lap_result.theta_final,
            ref["theta"],
            rtol=0.02,
            err_msg="Laplacian THETA deviates >2% from reference",
        )

    def test_laplacian_ofv_ge_foce_minus_tolerance(self, foce_result, lap_result):
        """Laplacian OFV should be >= FOCE OFV - 2.0 (Hessian correction ≥ 0)."""
        assert lap_result.ofv >= foce_result.ofv - 2.0, (
            f"Laplacian OFV {lap_result.ofv:.4f} < FOCE OFV {foce_result.ofv:.4f} - 2"
        )

    def test_laplacian_omega_psd(self, lap_result):
        eigvals = np.linalg.eigvalsh(lap_result.omega_final)
        assert np.all(eigvals >= -1e-8), f"Laplacian OMEGA not PSD: eigvals={eigvals}"


