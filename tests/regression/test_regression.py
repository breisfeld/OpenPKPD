@pytest.mark.regression
@pytest.mark.slow
class TestIMPRegression:
    """
    IMP estimation regression against self-consistent golden file.

    Verifies that the importance-sampling marginal-likelihood estimator:
      1. Produces a finite OFV.
      2. Matches a pre-computed self-consistent golden file (theophylline_imp.json)
         within a generous tolerance (IMP is a Monte Carlo estimator with inherent
         variance between runs).
      3. Reports convergence and finite post-hoc ETAs.
      4. THETA estimates are within 50% of FOCE final THETAs (IMP on this
         small dataset tends toward the initial values due to flat IS landscape).

    Reference dataset / parameters: same synthetic Theophylline design used
    throughout this module (seed=42, 12 subjects, ADVAN2/TRANS2, isample=200).
    """

    @pytest.fixture(scope="class")
    def imp_result(self):
        _, params, pop_model = _build_theophylline_model()
        method = IMPMethod(isample=200, maxeval=150, seed=42, print_interval=9999)
        return method.estimate(pop_model, params)

    def test_imp_ofv_finite(self, imp_result):
        assert np.isfinite(imp_result.ofv), f"IMP OFV not finite: {imp_result.ofv}"

    def test_imp_ofv_matches_reference(self, imp_result):
        ref = load_reference("theophylline_imp")
        # IS estimator is noisy: allow ±50 OFV units
        assert abs(imp_result.ofv - ref["ofv"]) < 50.0, (
            f"IMP OFV {imp_result.ofv:.4f} differs from reference "
            f"{ref['ofv']:.4f} by {abs(imp_result.ofv - ref['ofv']):.4f}"
        )

    def test_imp_theta_matches_reference(self, imp_result):
        ref = load_reference("theophylline_imp")
        np.testing.assert_allclose(
            imp_result.theta_final,
            ref["theta"],
            rtol=0.05,
            err_msg="IMP THETA deviates >5% from reference",
        )

    def test_imp_converged(self, imp_result):
        assert imp_result.converged is True, "IMP should report convergence"

    def test_imp_post_hoc_etas_finite(self, imp_result):
        for sid, eta in imp_result.post_hoc_etas.items():
            assert np.all(np.isfinite(eta)), f"Non-finite IMP ETA for subject {sid}: {eta}"

    def test_imp_omega_psd(self, imp_result):
        eigvals = np.linalg.eigvalsh(imp_result.omega_final)
        assert np.all(eigvals >= -1e-8), f"IMP OMEGA not PSD: eigvals={eigvals}"

    def test_imp_sigma_positive(self, imp_result):
        diag = np.diag(imp_result.sigma_final)
        assert np.all(diag > 0), f"IMP SIGMA diagonal not all positive: {diag}"

    def test_imp_ofv_history_nonempty(self, imp_result):
        assert len(imp_result.ofv_history) > 0, "IMP OFV history should be non-empty"


